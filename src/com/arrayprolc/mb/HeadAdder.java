	public static String allNames() {
		String s = "";
		for (String key : map.keySet()) {
			s = s + key + "§7, §e";
		}
		return s;
	}

	@SuppressWarnings("deprecation")
	public static void getHead(String string, Player player) {
		Boolean b = false;
		if (string.equalsIgnoreCase("me")) {
			string = player.getName();
			b = true;
		} else if (string.equalsIgnoreCase("hand")) {
			string = player.getItemInHand().getItemMeta().hasDisplayName() ? player
					.getItemInHand().getItemMeta().getDisplayName()
					: player.getItemInHand().getType().toString()
							.replace("_", " ").toLowerCase();
			string = StringTools.convert(string);
		}
		if (!map.containsKey(string) && !map.containsKey(string.toLowerCase())) {
			b = true;
		} else {
			string = string.toLowerCase();
		}
		ItemStack skull = new ItemStack(397, 1, (short) 3);
		SkullMeta meta = (SkullMeta) skull.getItemMeta();
		String l = "";
		if (!b) {
			meta.setOwner(map.get(string));
			l = "§6Mini §e" + string;
			meta.setDisplayName(l);
		} else {
			l = "§e" + string + "'s §6head";
			meta.setOwner(string);
			meta.setDisplayName(l);
		}
		skull.setItemMeta(meta);
		player.getInventory().addItem(skull);
		String c = "";
		if (!b) {
			c = "a ";
		}
		player.sendMessage("§e" + Main.pluginname + " >§7 You recieved " + c
				+ l.toLowerCase() + "§7.");
	}

